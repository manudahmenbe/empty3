package com.javafx.experiments.utils3d.geom;

/**
 * The <code>Dimension2D</code> class is to encapsulate a width
 * and a height dimension.
 * <p>
 */
public class Dimension2D {
    public float width;
    public float height;

    public Dimension2D() {
    }

    public Dimension2D(float w, float h) {
        width = w;
        height = h;
    }
}
